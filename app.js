

var перше_число = prompt("Введіть перше число:");
var друге_число = prompt("Введіть друге число:");

перше_число = parseFloat(перше_число);
друге_число = parseFloat(друге_число);


var сума = перше_число + друге_число;

const різниця = перше_число - друге_число;

const добуток = перше_число * друге_число;

const частка = перше_число / друге_число;

alert(`Сума чисел: ${сума} \n
       Різниця чисел: ${різниця} \n
       Добуток чисел: ${добуток} \n
       Частка чисел: ${частка}
       `);